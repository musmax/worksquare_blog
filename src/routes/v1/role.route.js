const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const { roleValidation } = require('../../validations');
const { roleController } = require('../../controllers');

const router = express.Router();

router
  .route('/')
  .post(auth('roles.manage'), validate(roleValidation.createRole), roleController.createRole)
  .get(auth('roles.view'), roleController.getRoles);

router
  .route('/:roleId')
  .get(auth('roles.view'), validate(roleValidation.getRole), roleController.getRoleById)
  .patch(auth('roles.manage'), validate(roleValidation.updateRole), roleController.updateRoleById)
  .delete(auth('roles.manage'), validate(roleValidation.deleteRole), roleController.deleteRoleById);

module.exports = router;

